public class PatternQ35 {
    public static void main(String[] args) {
        for (int i = 0; i < 9; i++) {
            System.out.print("* ");
        }
        System.out.println();
        for (int i = 0; i < 5; i++) {
            System.out.println("        *");
        }
        for (int i = 0; i < 9; i++) {
            System.out.print("* ");
        }
    }
}
